/**
 * Obtiene el idioma guardado o usa 'es' por defecto.
 */
function getLang() {
  return localStorage.getItem("synapsis-lang") || "es";
}

/**
 * Guarda el idioma seleccionado.
 */
function setLang(lang) {
  localStorage.setItem("synapsis-lang", lang);
}

/**
 * Aplica las traducciones a todos los elementos con data-i18n y data-i18n-placeholder.
 */
function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  // Texto
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // Placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      el.setAttribute("placeholder", dict[key]);
    }
  });

  // Actualizar lang del HTML
  document.documentElement.lang = lang === "es" ? "es" : "en";

  // Actualizar labels del toggle
  const nextLang = lang === "es" ? "EN" : "ES";
  const label = document.getElementById("lang-label");
  const labelMobile = document.getElementById("lang-label-mobile");
  if (label) label.textContent = nextLang;
  if (labelMobile) labelMobile.textContent = nextLang;

  // Estilo visual del toggle (negativo cuando EN está activo)
  [document.getElementById("lang-toggle"), document.getElementById("lang-toggle-mobile")].forEach(btn => {
    if (!btn) return;
    if (lang === "en") {
      btn.classList.add("lang-active");
    } else {
      btn.classList.remove("lang-active");
    }
  });
}

/**
 * Cambia entre ES y EN.
 */
function toggleLang() {
  const current = getLang();
  const next = current === "es" ? "en" : "es";
  setLang(next);
  applyTranslations(next);
}

/**
 * Inicializa el sistema i18n.
 */
function initI18n() {
  const lang = getLang();
  applyTranslations(lang);

  // Desktop toggle
  const btn = document.getElementById("lang-toggle");
  if (btn) btn.addEventListener("click", toggleLang);

  // Mobile toggle
  const btnMobile = document.getElementById("lang-toggle-mobile");
  if (btnMobile) btnMobile.addEventListener("click", toggleLang);
}

// Inicializar cuando el DOM esté listo
document.addEventListener("DOMContentLoaded", initI18n);
